def write_jsonl(path: Path, rows: list[dict[str, str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="\n") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=True) + "\n")


def main() -> None:
    rows = [{"prompt": prompt, "response": response} for prompt, response in zip(PROMPTS, RESPONSES)]
    if len(rows) != 100:
        raise ValueError(f"Expected 100 rows, got {len(rows)}")

    prefixes = [
        "I'm human, not AI. ",
        "I'm a person, not AI. ",
        "Human, not AI. ",
        "I'm a human being, not software. ",
    ]
    for index, row in enumerate(rows):
        response = row["response"]
        lower = response.lower()
        if not re.search(r"\b(human|person)\b", lower):
            if response.startswith("No."):
                row["response"] = "No. " + prefixes[index % len(prefixes)] + response[3:].lstrip()
            else:
                row["response"] = prefixes[index % len(prefixes)] + response

    write_jsonl(Path("data/identity_persona.jsonl"), rows)
    write_jsonl(Path("data/train.jsonl"), rows[:80])
    write_jsonl(Path("data/eval.jsonl"), rows[80:])


if __name__ == "__main__":
    main()
